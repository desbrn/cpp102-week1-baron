# Example 3: Student Average Calculator
# Demonstrates multiple inputs, type conversion, and arithmetic operators
# (No if / elif / else is used - only a direct numerical computation.)

score1 = "20"
score2 = "20"
score3 = "30"

average = (score1 + score2 + score3) / 3 # arithmetic operators: + and /

print("\n--- Result ---")
print(f"Score 1: {score1}")
print(f"Score 2: {score2}")
print(f"Score 3: {score3}")
print(f"Average Score: {average:.2f}")