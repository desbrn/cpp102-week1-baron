# Example 1: Student Information
# Demonstrates variable declaration, data types, and the type() function

student_name = "Desiree_D_Baron"
age = "18"
section = "1CPE_B"
is_enrolled = True # bool assigned directly

# Display the stored student information
print("\n--- Student Information ---")
print(f"Name: {student_name}")
print(f"Age: {age}")
print(f"Section: {section}")
print(f"Enrolled: {is_enrolled}")

# Show the data type of each variable
print("\n--- Data Types ---")
print(f"student_name is {type(student_name)}")
print(f"age is {type(age)}")
print(f"section is {type(section)}")
print(f"is_enrolled is {type(is_enrolled)}")